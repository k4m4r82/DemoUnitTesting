using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace CalculatorLib
{
    public class Calculator
    {
        public int Tambah(int a, int b)
        {
            return a + b;
        }

        public int Kurang(int a, int b)
        {
            return a - b;
        }

        public int Kali(int a, int b)
        {
            return a * b;
        }

        public int Bagi(int a, int b)
        {
            return a / b;
        }
    }
}
